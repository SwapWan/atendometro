            <header>O que é NPS</header>
            <p>Desenvolvido em 2003 por Fred Reichheld, <a href="https://www.atendometro.me/faq.php">NPS</a> (Net Promoter Score) é uma metodologia de pesquisa utilizada para mensurar o nível de satisfação e lealdade dos clientes. Seu objetivo é saber a probabilidade dos clientes recomendarem a sua marca para amigos e familiares.</p>
        </section>
