        <section>
            <header>Como surgiu</header>
            <p>O surgimento do <a href="https://www.atendometro.me/faq.php">NPS</a> veio pela busca de conhecer melhor o cliente.</p>
            <p>O ideal seria descobrir o nível de lealdade do consumidor, levando em conta a probabilidade de indicação de um negócio para pessoas próximas.</p>
        </section>
        <br>
        <section>
            <header>Importância</header>
            <p>A importância desta ferramenta se dá na procura por um indicador que demonstre o nível de satisfação, ou insatisfação, do cliente.</p>
            <p>Atualmente, é considerada uma métrica essencial de customer success.</p>
            <p>Este, no caso, é a busca de altíssima qualidade na experiência do cliente, atingindo altas taxas de retenção e tendo menos cancelamentos.</p>
